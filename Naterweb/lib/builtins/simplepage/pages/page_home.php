<h3>Hey there, and welcome!</h3>
<p>If you're seeing this page (and it looks as expected), then you've successfully installed Thenaterweb 
	on your server. This is the default homepage for the page application, a Naterweb application that 
	renders basic pages for you. To replace it and add your own pages, you can add and replace the existing 
	pages in the controller/page/pages directory, following the same naming scheme or you can change the page 
	settings to a different directory and place them there.
</p>

<ul>
	<lh><strong>What now?</strong></lh>
	<li>Make sure you've properly configured thenaterweb by updating the settings.php file, and set up 
	the .htaccess file for your server correctly if you intend to use friendly urls.</li>
	<li>Configure the authentication app so you have access to the admin panel if you intend to use it 
		or intend to offer a service with logins.</li>
	<li><a href="/?url=webadmin">Log into the administration page 
		to manage your site</a>.</li>
	<li><a href="https://github.com/thenaterhood/thenaterweb">Visit Thenaterweb on github for documentation 
		or to contribute</a>.</li>
</ul>

<p>If you intend to change the template of your apps, you can configure them to use a template other than the 
	default included one (though it is recommended you leave that one as-is) by changing their template 
	path in their configuration files.</p>
